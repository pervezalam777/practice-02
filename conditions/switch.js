let number = 1;
switch(number){ 
  case '1': // number === '1'
    console.log('in string for -> one')
    break;
  case 1: // number === 1
    console.log('one');
    break;
  case 2: // number === 2
    console.log('two');
    break;
  case 3:
    console.log('three');
    break;
  case 4:
    console.log('four');
    break;
  default:
    console.log('not match found')
}

